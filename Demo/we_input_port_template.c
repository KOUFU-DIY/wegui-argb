#include "we_input_port_template.h"

/* --------------------------------------------------------------------------
 * WeGui 输入端口移植模板实现
 *
 * 这份 .c 文件给输入链路做最小可改骨架。
 *
 * 和 LCD 端口模板不同，输入端口这里默认不使用 while(1)：
 * 1. 因为很多项目本来就没有触摸/按键输入
 * 2. 这时“无输入设备”属于正常场景，不应被当成错误
 * 3. 所以这里采用“安全空实现”，默认返回无输入状态
 * -------------------------------------------------------------------------- */

/**
 * @brief 初始化输入硬件链路。
 * @return 无。
 */
void input_hw_init(void)
{
    /* 默认空实现：
     * 1. 没有输入设备时，不需要做任何初始化
     * 2. 真正移植到触摸/按键/编码器时，再在这里补硬件初始化 */
}

/**
 * @brief 读取输入硬件状态并填充输入事件数据。
 * @param data 输入状态输出结构体指针。
 * @return 无。
 */
void we_input_port_read(we_indev_data_t *data)
{
    if (data == NULL)
    {
        return;
    }

    /* 默认返回“当前没有输入事件”。
     * 这样即使项目没有输入设备，GUI 仍然可以正常运行。 */
    data->x = 0;
    data->y = 0;
    data->state = WE_TOUCH_STATE_NONE;
}
